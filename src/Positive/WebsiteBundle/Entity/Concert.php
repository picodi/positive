     * @var string
     */
    private $address;


    /**
     * Set address
     *
     * @param string $address
     * @return Concert
     */
    public function setAddress($address)
    {
        $this->address = $address;

        return $this;
    }

    /**
     * Get address
     *
     * @return string 
     */
    public function getAddress()
    {
        return $this->address;
